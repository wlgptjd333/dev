# 변수란?
'''
변수명: 숫자, 몇몇(_)제외한 특수문자로 시작 X
'''

# 타입(자료형) 4가지
'''
int     : 정수
float   : 실수
str     : 문자열(파이썬은 문자와 문자열 구분 X, 아무 따옴표 필요)
bool    : 논리값(True/False)
'''

age = 18
print(age)
print(type(age))

height = 187.3
print(height)
print(type(height))

이름 = "홍길동"
print(이름)
print(type(이름))

is_adult = True
print(is_adult)
print(type(is_adult))
